//字段隐藏或显示
function isView(fields,field){
	
	if(contains(fields,field)){
		$("#"+field).css("display","none");
		if(field=="edit"){
			//$("."+field).css("display","none");
			$("#tr_changeEditor").css("display","none");
		}
		var caption = field.replace("tr","Caption");
		$("#"+caption).css("display","none");

	}else{
		$("#"+field).css("display","");
		if(field=="edit"){
			//$("."+field).css("display","");
			$("#tr_changeEditor").css("display","");
		}
		var caption = field.replace("tr","Caption");
		$("#"+caption).css("display","");
	}
	
}
//是否包含
function contains(fields,field){
	var flag = false ;
	
	for(var i= 0;i<fields.length;i++){
		
		if(field==fields[i]){
						
			flag = true;
		}
	}
	return flag ;
}
